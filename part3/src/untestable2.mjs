function diceRoll() {
  const min = 1;
  const max = 6;
  return Math.floor(Math.random() * (max + 1 - min) + min);
}

export function diceHandValue(dice1, dice2) {
  if (dice1 === dice2) {
    // one pair
    return 100 + dice1;
  } else {
    // high die
    return Math.max(dice1, dice2);
  }
}
